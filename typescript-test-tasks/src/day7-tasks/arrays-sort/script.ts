// Sortiere die Programmiersprachen in umgekehrter alphabetischer Reihenfolge.

const codingLanguages: string[] = [
  "JavaScript",
  "Python",
  "Java",
  "Ruby",
  "PHP",
  "C++",
  "CSS",
  "C#",
  "Go",
  "C",
  "TypeScript",
  "Swift",
];

function sortDescending() {
  return codingLanguages.sort().reverse();
}

console.log(sortDescending());

